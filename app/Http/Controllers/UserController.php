    public function updatePassword(Request $request, User $user)
    {
        $request->validate([
            'password' =>'required|confirmed'
        ]);

        User::find($user->id)->update(['password'=> Hash::make($request->password)]);
        Alert::success('Password Updated', 'Success Message');
        return back();
